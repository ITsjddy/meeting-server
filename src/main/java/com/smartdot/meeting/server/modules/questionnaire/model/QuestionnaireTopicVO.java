package com.smartdot.meeting.server.modules.questionnaire.model;

import java.util.List;

public class QuestionnaireTopicVO {
	private String id;
	private String topicNumber;
	private String topic;
	private String notes;
	private String topicType;
	private String questionnaireId;
	
	private List<QuestionnaireOptionVO> options;

	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}

	public String getTopicNumber(){
		return this.topicNumber;
	}

	public String getTopic(){
		return this.topic;
	}

	public String getNotes(){
		return this.notes;
	}

	public String getTopicType(){
		return this.topicType;
	}

	public String getQuestionnaireId(){
		return this.questionnaireId;
	}

	public void setTopicNumber(String topicnumber){
		this.topicNumber=topicnumber;
	}

	public void setTopic(String topic){
		this.topic=topic;
	}

	public void setNotes(String notes){
		this.notes=notes;
	}

	public void setTopicType(String topictype){
		this.topicType=topictype;
	}

	public void setQuestionnaireId(String questionnaireid){
		this.questionnaireId=questionnaireid;
	}

	public List<QuestionnaireOptionVO> getOptions() {
		return options;
	}

	public void setOptions(List<QuestionnaireOptionVO> options) {
		this.options = options;
	}
	
}

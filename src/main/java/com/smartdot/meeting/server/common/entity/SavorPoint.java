package com.smartdot.meeting.server.common.entity;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import com.smartdot.meeting.server.common.entity.BaseEntity;

/**
 * 
 * 
 * @ClassName: SavorPoint
 * @Description: 兴趣点管理（酒店）
 * @author: haomt
 * @date: 2017年1月6日 上午10:14:48
 */
@Entity
@Table(name = "T_SAVORPOINT")
public class SavorPoint extends BaseEntity {

	/**
	 * @fieldName: serialVersionUID
	 * @fieldType: long
	 */
	private static final long serialVersionUID = -37525448916170885L;

	/**
	 * 兴趣点唯一标识
	 */
	private String spOnly;

	@Column(name = "SP_ONLY")
	public String getSpOnly() {
		return spOnly;
	}

	public void setSpOnly(String spOnly) {
		this.spOnly = spOnly;
	}

	/**
	 * 兴趣点名称
	 */
	private String spName;

	@Column(name = "SP_NAME")
	public String getSpName() {
		return spName;
	}

	public void setSpName(String spName) {
		this.spName = spName;
	}


	/**
	 * 兴趣点中文地址
	 */
	private String spAddress;

	@Column(name = "SP_ADDRESS")
	public String getSpAddress() {
		return spAddress;
	}

	public void setSpAddress(String spAddress) {
		this.spAddress = spAddress;
	}

	/**
	 * 兴趣点的经度
	 */
	private String spLongitude;

	@Column(name = "SP_LONGITUDE")
	public String getSpLongitude() {
		return spLongitude;
	}

	public void setSpLongitude(String spLongitude) {
		this.spLongitude = spLongitude;
	}

	/**
	 * 兴趣点的维度
	 */
	private String spDimensions;

	@Column(name = "SP_DIMENSIONS")
	public String getSpDimensions() {
		return spDimensions;
	}

	public void setSpDimensions(String spDimensions) {
		this.spDimensions = spDimensions;
	}

	/**
	 * 兴趣点的LOGO
	 */
	private String spLogo;

	@Column(name = "SP_LOGO")
	public String getSpLogo() {
		return spLogo;
	}

	public void setSpLogo(String spLogo) {
		this.spLogo = spLogo;
	}

	/**
	 * 兴趣点的类型
	 */
	private String spType;

	@Column(name = "SP_TYPE")
	public String getSpType() {
		return spType;
	}

	public void setSpType(String spType) {
		this.spType = spType;
	}
	
	/**
	 * 语言类型
	 */
	private String spLanguageType;

	@Column(name = "SP_LANGUAGE_TYPE")
	public String getSpLanguageType() {
		return spLanguageType;
	}

	public void setSpLanguageType(String spLanguageType) {
		this.spLanguageType = spLanguageType;
	}
	
	/**
	 * 语言类型
	 */
	private String spUniteId;
	
	@Column(name = "SP_UNITE_ID")
	public String getSpUniteId() {
		return spUniteId;
	}
	
	public void setSpUniteId(String spUniteId) {
		this.spUniteId = spUniteId;
	}

}

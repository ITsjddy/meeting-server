package com.smartdot.meeting.server.common.entity;


import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;




/** 
 * @ClassName: JpushEntiy 
 * @Description: 极光推送管理
 * @author: haomt
 * @date: 2017年1月12日 下午3:16:39  
 */
@Entity
@Table(name = "T_JPUSH_ENTIY")
public class JpushEntiy extends BaseEntity{

	/**
	 * @fieldName: serialVersionUID
	 * @fieldType: long
	 */
	private static final long serialVersionUID = 4070870369090724753L;
	
	
	/**
	 * @fieldName: broadCast
	 * @fieldType: String
	 * @Description: 广播
	 */
	private String broadCast;

	@Column(name = "BROAD_CAST")
	public String getBroadCast() {
		return broadCast;
	}


	public void setBroadCast(String broadCast) {
		this.broadCast = broadCast;
	}
	
	/**
	 * @fieldName: insiderIds
	 * @fieldType: String
	 * @Description: 内部人员
	 */
	private String insiderIds;

	@Column(name = "INSIDER_IDS")
	public String getInsiderIds() {
		return insiderIds;
	}


	public void setInsiderIds(String insiderIds) {
		this.insiderIds = insiderIds;
	}
	
	/**
	 * @fieldName: outsiderIds
	 * @fieldType: String
	 * @Description: 外部人员
	 */
	private String outsiderIds;

	@Column(name = "OUTSIDER_IDS")
	public String getOutsiderIds() {
		return outsiderIds;
	}


	public void setOutsiderIds(String outsiderIds) {
		this.outsiderIds = outsiderIds;
	}
	
	/**
	 * @fieldName: jpTitle
	 * @fieldType: String
	 * @Description: 推送的标题
	 */
	private String jpTitle;

	@Column(name = "JP_TITLE")
	public String getJpTitle() {
		return jpTitle;
	}

	public void setJpTitle(String jpTitle) {
		this.jpTitle = jpTitle;
	}

	/**
	 * @fieldName: jpText
	 * @fieldType: String
	 * @Description: 推送的正文
	 */
	private String jpText;

	@Column(name = "JP_TEXT")
	public String getJpText() {
		return jpText;
	}

	public void setJpText(String jpText) {
		this.jpText = jpText;
	}
	
	/**
	 * @fieldName: languageId
	 * @fieldType: String
	 * @Description: 语言标识
	 */
	private String languageId;
	
	@Column(name = "LANGUAGE_ID")
	public String getLanguageId() {
		return languageId;
	}

	public void setLanguageId(String languageId) {
		this.languageId = languageId;
	}
	
	
	/**
	 * @fieldName: JpUniteId
	 * @fieldType: String
	 * @Description: 公共ID
	 */
	private String jpUniteId;
	
	@Column(name = "JP_UNITE_ID")
	public String getJpUniteId() {
		return jpUniteId;
	}
	
	public void setJpUniteId(String jpUniteId) {
		this.jpUniteId = jpUniteId;
	}
	
	/**
	 * @fieldName: jpUserId
	 * @fieldType: String
	 * @Description: 公共ID
	 */
	private String jpUserId;

	@Column(name = "JP_USER_ID")
	public String getJpUserId() {
		return jpUserId;
	}


	public void setJpUserId(String jpUserId) {
		this.jpUserId = jpUserId;
	}
	
	
	
	
}

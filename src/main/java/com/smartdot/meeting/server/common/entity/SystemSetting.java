package com.smartdot.meeting.server.common.entity;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

/** 
 * @ClassName: SystemSetting 
 * @Description: 系统配置实体
 * @author: haot
 * @date: 2017年3月6日 下午5:05:08  
 */

@Entity
@Table(name = "T_SYSTEM_SETTING")
public class SystemSetting extends BaseEntity {

	/**
	 * @fieldName: serialVersionUID
	 * @fieldType: long
	 * @Description: TODO
	 */
	private static final long serialVersionUID = -4361976474159912167L;
	
	/**
	 * @fieldName: uniqueId
	 * @fieldType: String
	 * @Description: 唯一标识
	 */
	private String uniqueId;
	@Column(name = "SS_UNIQUEID")
	public String getUniqueId() {
		return uniqueId;
	}
	public void setUniqueId(String uniqueId) {
		this.uniqueId = uniqueId;
	}
	/**
	 * @fieldName: content
	 * @fieldType: String
	 * @Description: 内容
	 */
	private String content;
	@Column(name = "SS_CONTENT")
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	/**
	 * @fieldName: status
	 * @fieldType: String
	 * @Description: 状态
	 */
	private String status;
	@Column(name = "SS_STATUS")
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	/**
	 * @fieldName: description
	 * @fieldType: String
	 * @Description: 说明
	 */
	private String description;
	@Column(name = "SS_DESCRIPTION")
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	

}
